// Crie um código que utilize um loop while para contar de 1 a 10 e exibir cada número no console.

// Objetivo:
// Aprender a utilizar a estrutura de repetição while para executar um bloco de código repetidamente enquanto uma condição é verdadeira.

let contador = 1;

while (contador <= 10) {
  console.log(contador);
  contador++;
}